#include <bits/stdc++.h>
using namespace std;

void solve() {
    int n;
    cin >> n;

    vector<int> a(n), c(n);

    for (int i = 0; i < n; i++)
    {
        a[i];
    }
    
    for (int i = 0; i < n; i++)
    {
        c[i];
    }
    
}

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(nullptr);
    
    int tc;
    cin >> tc;

    while(tc--) {
        solve();
    }
}